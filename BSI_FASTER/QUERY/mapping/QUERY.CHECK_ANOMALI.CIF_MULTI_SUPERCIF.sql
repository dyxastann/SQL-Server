WITH CTE AS
(
	SELECT
		[NOMORCIF] [NOMORCIF]
		,[SUPERCIF] [SUPERCIF]
	FROM [mapping].[PORTOFOLIO.ONBS.CIF_DETAIL]
	GROUP BY
		[NOMORCIF]
		,[SUPERCIF]
)
SELECT
	*
FROM CTE
WHERE [NOMORCIF]
IN (
	SELECT
		[NOMORCIF]
	FROM CTE
	GROUP BY
		[NOMORCIF]
	HAVING COUNT(1) > 1
)
ORDER BY
	[NOMORCIF]
	,[SUPERCIF]