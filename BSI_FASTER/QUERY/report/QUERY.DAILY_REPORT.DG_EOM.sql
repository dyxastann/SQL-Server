			,SUM(CASE WHEN COALESCE([LM].[KOL_SUPERCIF], [M_1].[KOL_SUPERCIF], ''1'') IN (''1'') AND COALESCE([BEF].[KOL_SUPERCIF], [D_1].[KOL_SUPERCIF], ''1'') IN (''1'') AND LEFT([CUR].[KOL_SUPERCIF], 1) IN (''2'') THEN 1 ELSE 0 END) [ACCT_KOL_1_TO_2]
			,SUM(CASE WHEN COALESCE([LM].[KOL_SUPERCIF], [M_1].[KOL_SUPERCIF], ''1'') IN (''1'') AND COALESCE([BEF].[KOL_SUPERCIF], [D_1].[KOL_SUPERCIF], ''1'') IN (''1'') AND LEFT([CUR].[KOL_SUPERCIF], 1) IN (''2'') THEN [CUR].[OS_POKOK_PSAK] ELSE 0.00 END) / 1000000000.00 [OS_KOL_1_TO_2]
			,SUM(CASE WHEN LEFT(COALESCE([LM].[KOL_SUPERCIF], [M_1].[KOL_SUPERCIF], ''1''), 1) IN (''1'', ''2'') AND LEFT(COALESCE([BEF].[KOL_SUPERCIF], [D_1].[KOL_SUPERCIF], ''1''), 1) IN (''1'', ''2'') AND LEFT([CUR].[KOL_SUPERCIF], 1) IN (''3'', ''4'', ''5'') THEN 1 ELSE 0 END) [ACCT_PF_TO_NPF]
			,SUM(CASE WHEN LEFT(COALESCE([LM].[KOL_SUPERCIF], [M_1].[KOL_SUPERCIF], ''1''), 1) IN (''1'', ''2'') AND LEFT(COALESCE([BEF].[KOL_SUPERCIF], [D_1].[KOL_SUPERCIF], ''1''), 1) IN (''1'', ''2'') AND LEFT([CUR].[KOL_SUPERCIF], 1) IN (''3'', ''4'', ''5'') THEN [CUR].[OS_POKOK_PSAK] ELSE 0.00 END) / 1000000000.00 [OS_PF_TO_NPF]
		FROM ', @TARGET, ' [CUR]
		FULL OUTER JOIN ', @TARGET_BEFORE, ' [BEF]
			ON [CUR].[NOLOAN] = [BEF].[NOLOAN]
		FULL OUTER JOIN ', @TARGET_LM, ' [LM]
			ON [CUR].[NOLOAN] = [LM].[NOLOAN]
		OUTER APPLY (
			SELECT
				COALESCE(MAX([BY_LOAN]), MAX([BY_CIF]) , MAX([BY_SUPERCIF])) [KOL_SUPERCIF]
			FROM (
				SELECT
					CASE WHEN [NOLOAN] IN (
						SELECT
							[NOLOAN_LAMA]
						FROM [mapping].[PORTOFOLIO_ONBS.COMPILE.JOIN.REKAP_NOLOAN.MUTASI_NOLOAN]
						WHERE [NOLOAN_BARU] = [CUR].[NOLOAN]
						AND COALESCE([KETERANGAN], ''X'') NOT IN (''Invalid'')
					) OR [NOLOAN] = COALESCE([CUR].[NOLOAN], ''X'') THEN [KOL_SUPERCIF]
					WHEN [CUR].[DIVISI] IN (''Pawning'') THEN ''1'' END [BY_LOAN]
					,CASE WHEN [NOMORCIF] = [CUR].[NOMORCIF] THEN [KOL_SUPERCIF] END [BY_CIF]
					,[KOL_SUPERCIF] [BY_SUPERCIF]
				FROM ', @TARGET_LM, '
				WHERE [CUR].[SUPERCIF] = [SUPERCIF]
					AND [CUR].[DIVISI] = [DIVISI]
			) [SUB]
		) [M_1]
		OUTER APPLY (
			SELECT
				COALESCE(MAX([BY_LOAN]), MAX([BY_CIF]) , MAX([BY_SUPERCIF])) [KOL_SUPERCIF]
			FROM (
				SELECT
					CASE WHEN [NOLOAN] IN (
						SELECT
							[NOLOAN_LAMA]
						FROM [mapping].[PORTOFOLIO_ONBS.COMPILE.JOIN.REKAP_NOLOAN.MUTASI_NOLOAN]
						WHERE [NOLOAN_BARU] = [CUR].[NOLOAN]
						AND COALESCE([KETERANGAN], ''X'') NOT IN (''Invalid'')
					) OR [NOLOAN] = COALESCE([CUR].[NOLOAN], ''X'') THEN [KOL_SUPERCIF]
					WHEN [CUR].[DIVISI] IN (''Pawning'') THEN ''1'' END [BY_LOAN]
					,CASE WHEN [NOMORCIF] = [CUR].[NOMORCIF] THEN [KOL_SUPERCIF] END [BY_CIF]
					,[KOL_SUPERCIF] [BY_SUPERCIF]
				FROM ', @TARGET_BEFORE, '
				WHERE [CUR].[SUPERCIF] = [SUPERCIF]
				AND
				(
					(
						[CUR].[DIVISI] IN (''Hasanah Card'')
						AND [DIVISI] IN (''Hasanah Card'')
					)
					OR
					(
						[CUR].[DIVISI] NOT IN (''Hasanah Card'')
						AND [DIVISI] NOT IN (''Hasanah Card'')
					)
				)
			) [SUB]
		) [D_1]
		WHERE COALESCE([CUR].[DIVISI], [BEF].[DIVISI]) IN (''Konsumer'', ''Hasanah Card'', ''Pawning'', ''SME'', ''Mikro'')
-- 		AND COALESCE([CUR].[DIVISI], [BEF].[DIVISI]) IN (''Mikro'')
-- 		AND COALESCE([CUR].[PRODUK], [BEF].[PRODUK]) LIKE (''%Pensiun%'')
-- 		AND COALESCE([CUR].[PRODUK_DETAIL], [BEF].[PRODUK_DETAIL]) LIKE (''%FLPP%'')
		AND LEFT([CUR].[KOL_SUPERCIF], 1) IN (''2'', ''3'', ''4'', ''5'');'
	);
	
	INSERT INTO @RESULT EXEC(@SQL_QUERY);
	
	SET @tgl_data = DATEADD(DAY, 1, @tgl_data);
	SET @pointer = @pointer + 1;
END

SELECT * FROM @RESULT;