			,SUM(CASE WHEN COALESCE([LM].[KOL_SUPERCIF], [M_1].[KOL_SUPERCIF], ''1'') IN (''2B'') AND COALESCE([BEF].[KOL_SUPERCIF], [D_1].[KOL_SUPERCIF], ''1'') IN (''2B'') AND [CUR].[KOL_SUPERCIF] IN (''2C'') THEN [CUR].[OS_POKOK_PSAK] ELSE 0.00 END) / 1000000000.00 [OS_30_TO_60]
			,SUM(CASE WHEN COALESCE([LM].[KOL_SUPERCIF], [M_1].[KOL_SUPERCIF], ''1'') IN (''2C'') AND COALESCE([BEF].[KOL_SUPERCIF], [D_1].[KOL_SUPERCIF], ''1'') IN (''2C'') AND [CUR].[KOL_SUPERCIF] IN (''3A'') THEN 1 ELSE 0 END) [ACCT_60_TO_90]
			,SUM(CASE WHEN COALESCE([LM].[KOL_SUPERCIF], [M_1].[KOL_SUPERCIF], ''1'') IN (''2C'') AND COALESCE([BEF].[KOL_SUPERCIF], [D_1].[KOL_SUPERCIF], ''1'') IN (''2C'') AND [CUR].[KOL_SUPERCIF] IN (''3A'') THEN [CUR].[OS_POKOK_PSAK] ELSE 0.00 END) / 1000000000.00 [OS_60_TO_90]
			,SUM(CASE WHEN COALESCE([LM].[KOL_SUPERCIF], [M_1].[KOL_SUPERCIF], ''1'') IN (''3A'') AND COALESCE([BEF].[KOL_SUPERCIF], [D_1].[KOL_SUPERCIF], ''1'') IN (''3A'') AND [CUR].[KOL_SUPERCIF] IN (''3B'') THEN 1 ELSE 0 END) [ACCT_90_TO_120]
			,SUM(CASE WHEN COALESCE([LM].[KOL_SUPERCIF], [M_1].[KOL_SUPERCIF], ''1'') IN (''3A'') AND COALESCE([BEF].[KOL_SUPERCIF], [D_1].[KOL_SUPERCIF], ''1'') IN (''3A'') AND [CUR].[KOL_SUPERCIF] IN (''3B'') THEN [CUR].[OS_POKOK_PSAK] ELSE 0.00 END) / 1000000000.00 [OS_90_TO_120]
			,SUM(CASE WHEN COALESCE([LM].[KOL_SUPERCIF], [M_1].[KOL_SUPERCIF], ''1'') IN (''3B'') AND COALESCE([BEF].[KOL_SUPERCIF], [D_1].[KOL_SUPERCIF], ''1'') IN (''3B'') AND [CUR].[KOL_SUPERCIF] IN (''3C'') THEN 1 ELSE 0 END) [ACCT_120_TO_150]
			,SUM(CASE WHEN COALESCE([LM].[KOL_SUPERCIF], [M_1].[KOL_SUPERCIF], ''1'') IN (''3B'') AND COALESCE([BEF].[KOL_SUPERCIF], [D_1].[KOL_SUPERCIF], ''1'') IN (''3B'') AND [CUR].[KOL_SUPERCIF] IN (''3C'') THEN [CUR].[OS_POKOK_PSAK] ELSE 0.00 END) / 1000000000.00 [OS_120_TO_150]
			,SUM(CASE WHEN COALESCE([LM].[KOL_SUPERCIF], [M_1].[KOL_SUPERCIF], ''1'') IN (''3C'') AND COALESCE([BEF].[KOL_SUPERCIF], [D_1].[KOL_SUPERCIF], ''1'') IN (''3C'') AND [CUR].[KOL_SUPERCIF] IN (''4A'') THEN 1 ELSE 0 END) [ACCT_150_TO_180]
			,SUM(CASE WHEN COALESCE([LM].[KOL_SUPERCIF], [M_1].[KOL_SUPERCIF], ''1'') IN (''3C'') AND COALESCE([BEF].[KOL_SUPERCIF], [D_1].[KOL_SUPERCIF], ''1'') IN (''3C'') AND [CUR].[KOL_SUPERCIF] IN (''4A'') THEN [CUR].[OS_POKOK_PSAK] ELSE 0.00 END) / 1000000000.00 [OS_150_TO_180]
			,SUM(CASE WHEN COALESCE([LM].[KOL_SUPERCIF], [M_1].[KOL_SUPERCIF], ''1'') IN (''4A'') AND COALESCE([BEF].[KOL_SUPERCIF], [D_1].[KOL_SUPERCIF], ''1'') IN (''4A'') AND [CUR].[KOL_SUPERCIF] IN (''4B'') THEN 1 ELSE 0 END) [ACCT_180_TO_210]
			,SUM(CASE WHEN COALESCE([LM].[KOL_SUPERCIF], [M_1].[KOL_SUPERCIF], ''1'') IN (''4A'') AND COALESCE([BEF].[KOL_SUPERCIF], [D_1].[KOL_SUPERCIF], ''1'') IN (''4A'') AND [CUR].[KOL_SUPERCIF] IN (''4B'') THEN [CUR].[OS_POKOK_PSAK] ELSE 0.00 END) / 1000000000.00 [OS_180_TO_210]
			,SUM(CASE WHEN COALESCE([LM].[KOL_SUPERCIF], [M_1].[KOL_SUPERCIF], ''1'') IN (''4B'') AND COALESCE([BEF].[KOL_SUPERCIF], [D_1].[KOL_SUPERCIF], ''1'') IN (''4B'') AND [CUR].[KOL_SUPERCIF] IN (''4C'') THEN 1 ELSE 0 END) [ACCT_210_TO_240]
			,SUM(CASE WHEN COALESCE([LM].[KOL_SUPERCIF], [M_1].[KOL_SUPERCIF], ''1'') IN (''4B'') AND COALESCE([BEF].[KOL_SUPERCIF], [D_1].[KOL_SUPERCIF], ''1'') IN (''4B'') AND [CUR].[KOL_SUPERCIF] IN (''4C'') THEN [CUR].[OS_POKOK_PSAK] ELSE 0.00 END) / 1000000000.00 [OS_210_TO_240]
			,SUM(CASE WHEN COALESCE([LM].[KOL_SUPERCIF], [M_1].[KOL_SUPERCIF], ''1'') IN (''4C'') AND COALESCE([BEF].[KOL_SUPERCIF], [D_1].[KOL_SUPERCIF], ''1'') IN (''4C'') AND [CUR].[KOL_SUPERCIF] IN (''5'') THEN 1 ELSE 0 END) [ACCT_240_TO_270]
			,SUM(CASE WHEN COALESCE([LM].[KOL_SUPERCIF], [M_1].[KOL_SUPERCIF], ''1'') IN (''4C'') AND COALESCE([BEF].[KOL_SUPERCIF], [D_1].[KOL_SUPERCIF], ''1'') IN (''4C'') AND [CUR].[KOL_SUPERCIF] IN (''5'') THEN [CUR].[OS_POKOK_PSAK] ELSE 0.00 END) / 1000000000.00 [OS_240_TO_270]
			,SUM(CASE WHEN COALESCE([LM].[KOL_SUPERCIF], [M_1].[KOL_SUPERCIF], ''1'') IN (''5'') AND COALESCE([BEF].[KOL_SUPERCIF], [D_1].[KOL_SUPERCIF], ''1'') IN (''5'') AND [CUR].[KOL_SUPERCIF] IN (''5'') THEN 1 ELSE 0 END) [ACCT_270+]
