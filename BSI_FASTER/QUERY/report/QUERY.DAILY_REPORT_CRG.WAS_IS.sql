SELECT
	COALESCE([CUR].[KolCIF_BulanLalu], [LAST_MONTH].[KOLCIF], '01. Cair Baru') [KOLCIF]
	,SUM(CASE WHEN [CUR].[KOLCIF] IN ('1') THEN 1 ELSE 0 END) [ACCT_KOL_1]
	,SUM(CASE WHEN [CUR].[KOLCIF] IN ('2A') THEN 1 ELSE 0 END) [ACCT_KOL_2A]
	,SUM(CASE WHEN [CUR].[KOLCIF] IN ('2B') THEN 1 ELSE 0 END) [ACCT_KOL_2B]
	,SUM(CASE WHEN [CUR].[KOLCIF] IN ('2C') THEN 1 ELSE 0 END) [ACCT_KOL_2C]
	,SUM(CASE WHEN [CUR].[KOLCIF] IN ('3A') THEN 1 ELSE 0 END) [ACCT_KOL_3A]
	,SUM(CASE WHEN [CUR].[KOLCIF] IN ('3B') THEN 1 ELSE 0 END) [ACCT_KOL_3B]
	,SUM(CASE WHEN [CUR].[KOLCIF] IN ('3C') THEN 1 ELSE 0 END) [ACCT_KOL_3C]
	,SUM(CASE WHEN [CUR].[KOLCIF] IN ('4A') THEN 1 ELSE 0 END) [ACCT_KOL_4A]
	,SUM(CASE WHEN [CUR].[KOLCIF] IN ('4B') THEN 1 ELSE 0 END) [ACCT_KOL_4B]
	,SUM(CASE WHEN [CUR].[KOLCIF] IN ('4C') THEN 1 ELSE 0 END) [ACCT_KOL_4C]
	,SUM(CASE WHEN [CUR].[KOLCIF] IN ('5') THEN 1 ELSE 0 END) [ACCT_KOL_5]
	,SUM(CASE WHEN [CUR].[KOLCIF] IS NULL THEN 1 ELSE 0 END) [ACCT_RUN_OFF]
	,SUM(CASE WHEN [CUR].[KOLCIF] IN ('1') THEN [CUR].[OSPOKOKCONVERSION PSAK] ELSE 0.00 END) / 1000000000.0000000000 [OS_KOL_1]
	,SUM(CASE WHEN [CUR].[KOLCIF] IN ('2A') THEN [CUR].[OSPOKOKCONVERSION PSAK] ELSE 0.00 END) / 1000000000.0000000000 [OS_KOL_2A]
	,SUM(CASE WHEN [CUR].[KOLCIF] IN ('2B') THEN [CUR].[OSPOKOKCONVERSION PSAK] ELSE 0.00 END) / 1000000000.0000000000 [OS_KOL_2B]
	,SUM(CASE WHEN [CUR].[KOLCIF] IN ('2C') THEN [CUR].[OSPOKOKCONVERSION PSAK] ELSE 0.00 END) / 1000000000.0000000000 [OS_KOL_2C]
	,SUM(CASE WHEN [CUR].[KOLCIF] IN ('3A') THEN [CUR].[OSPOKOKCONVERSION PSAK] ELSE 0.00 END) / 1000000000.0000000000 [OS_KOL_3A]
	,SUM(CASE WHEN [CUR].[KOLCIF] IN ('3B') THEN [CUR].[OSPOKOKCONVERSION PSAK] ELSE 0.00 END) / 1000000000.0000000000 [OS_KOL_3B]
	,SUM(CASE WHEN [CUR].[KOLCIF] IN ('3C') THEN [CUR].[OSPOKOKCONVERSION PSAK] ELSE 0.00 END) / 1000000000.0000000000 [OS_KOL_3C]
	,SUM(CASE WHEN [CUR].[KOLCIF] IN ('4A') THEN [CUR].[OSPOKOKCONVERSION PSAK] ELSE 0.00 END) / 1000000000.0000000000 [OS_KOL_4A]
	,SUM(CASE WHEN [CUR].[KOLCIF] IN ('4B') THEN [CUR].[OSPOKOKCONVERSION PSAK] ELSE 0.00 END) / 1000000000.0000000000 [OS_KOL_4B]
	,SUM(CASE WHEN [CUR].[KOLCIF] IN ('4C') THEN [CUR].[OSPOKOKCONVERSION PSAK] ELSE 0.00 END) / 1000000000.0000000000 [OS_KOL_4C]
	,SUM(CASE WHEN [CUR].[KOLCIF] IN ('5') THEN [CUR].[OSPOKOKCONVERSION PSAK] ELSE 0.00 END) / 1000000000.0000000000 [OS_KOL_5]
	,SUM(CASE WHEN [CUR].[KOLCIF] IS NULL THEN [LAST_MONTH].[OSPOKOKCONVERSION PSAK] ELSE 0.00 END) / 1000000000.0000000000 [OS_RUN_OFF]
FROM [series].[CRG.LOAN_DAILY.2023-07-31] [CUR]
FULL OUTER JOIN [series].[CRG.LOAN_DAILY.2023-06-30 (FINAL)] [LAST_MONTH]
	ON [CUR].[NOLOAN] = [LAST_MONTH].[NOLOAN]
WHERE COALESCE([CUR].[SEGMEN_BSI], [LAST_MONTH].[SEGMEN_BSI]) IN ('Consumer', 'Hasanah Card', 'Pawning', 'Mikro', 'SME')
-- AND COALESCE([CUR].[SEGMEN_BSI], [LAST_MONTH].[SEGMEN_BSI]) IN ('Mikro')
-- AND COALESCE([CUR].[PRODUK_BSI], [LAST_MONTH].[PRODUK_BSI]) LIKE '%Pensiun%'
-- AND COALESCE([CUR].[PRODUK_BSI_DETAIL], [LAST_MONTH].[PRODUK_BSI_DETAIL]) LIKE '%FLPP%'
GROUP BY COALESCE([CUR].[KolCIF_BulanLalu], [LAST_MONTH].[KOLCIF], '01. Cair Baru')
ORDER BY COALESCE([CUR].[KolCIF_BulanLalu], [LAST_MONTH].[KOLCIF], '01. Cair Baru')