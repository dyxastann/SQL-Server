SELECT
	COALESCE([CYCLE].[CYCLE], 18) [CYCLE]
	,SUM(CASE WHEN LEFT([CRG].[KOLCIF], 1) IN ('1') THEN 1 ELSE 0 END) [ACCT_KOL_1]
	,SUM(CASE WHEN LEFT([CRG].[KOLCIF], 1) IN ('1') THEN [CRG].[OSPOKOKCONVERSION PSAK] ELSE 0.00 END) / 1000000000.000000000 [OS_KOL_1]
	,SUM(CASE WHEN LEFT([CRG].[KOLCIF], 1) IN ('2') THEN 1 ELSE 0 END) [ACCT_KOL_2]
	,SUM(CASE WHEN LEFT([CRG].[KOLCIF], 1) IN ('2') THEN [CRG].[OSPOKOKCONVERSION PSAK] ELSE 0.00 END) / 1000000000.000000000 [OS_KOL_2]
	,SUM(CASE WHEN [CRG].[KOLCIF] IN ('2A') THEN 1 ELSE 0 END) [ACCT_KOL_2A]
	,SUM(CASE WHEN [CRG].[KOLCIF] IN ('2A') THEN [CRG].[OSPOKOKCONVERSION PSAK] ELSE 0.00 END) / 1000000000.000000000 [OS_KOL_2A]
	,SUM(CASE WHEN [CRG].[KOLCIF] IN ('2B') THEN 1 ELSE 0 END) [ACCT_KOL_2B]
	,SUM(CASE WHEN [CRG].[KOLCIF] IN ('2B') THEN [CRG].[OSPOKOKCONVERSION PSAK] ELSE 0.00 END) / 1000000000.000000000 [OS_KOL_2B]
	,SUM(CASE WHEN [CRG].[KOLCIF] IN ('2C') THEN 1 ELSE 0 END) [ACCT_KOL_2C]
	,SUM(CASE WHEN [CRG].[KOLCIF] IN ('2C') THEN [CRG].[OSPOKOKCONVERSION PSAK] ELSE 0.00 END) / 1000000000.000000000 [OS_KOL_2C]
	,SUM(CASE WHEN LEFT([CRG].[KOLCIF], 1) IN ('3', '4', '5') THEN 1 ELSE 0 END) [ACCT_NPF]
	,SUM(CASE WHEN LEFT([CRG].[KOLCIF], 1) IN ('3', '4', '5') THEN [CRG].[OSPOKOKCONVERSION PSAK] ELSE 0.00 END) / 1000000000.000000000 [OS_NPF]
	,SUM(CASE WHEN LEFT([CRG].[KOLCIF], 1) IN ('3') THEN 1 ELSE 0 END) [ACCT_KOL_3]
	,SUM(CASE WHEN LEFT([CRG].[KOLCIF], 1) IN ('3') THEN [CRG].[OSPOKOKCONVERSION PSAK] ELSE 0.00 END) / 1000000000.000000000 [OS_KOL_3]
	,SUM(CASE WHEN LEFT([CRG].[KOLCIF], 1) IN ('4') THEN 1 ELSE 0 END) [ACCT_KOL_4]
	,SUM(CASE WHEN LEFT([CRG].[KOLCIF], 1) IN ('4') THEN [CRG].[OSPOKOKCONVERSION PSAK] ELSE 0.00 END) / 1000000000.000000000 [OS_KOL_4]
	,SUM(CASE WHEN LEFT([CRG].[KOLCIF], 1) IN ('5') THEN 1 ELSE 0 END) [ACCT_KOL_5]
	,SUM(CASE WHEN LEFT([CRG].[KOLCIF], 1) IN ('5') THEN [CRG].[OSPOKOKCONVERSION PSAK] ELSE 0.00 END) / 1000000000.000000000 [OS_KOL_5]
FROM [series].[CRG.LOAN_DAILY.2023-07-31] [CRG]
OUTER APPLY (
	SELECT TOP 1
		[CYCLE]
	FROM [series].[RCG.LOAN_DAILY.2023-07-31]
	WHERE [CRG].[NOLOAN] = [NOLOAN]
) [CYCLE]
WHERE [CRG].[SEGMEN_BSI] IN ('Consumer', 'Hasanah Card', 'Pawning', 'Mikro', 'SME')
-- AND [CRG].[SEGMEN_BSI] IN ('Mikro')
-- AND [CRG].[PRODUK_BSI] LIKE '%Pensiun%'
-- AND [CRG].[PRODUK_BSI_DETAIL] LIKE '%FLPP%'
GROUP BY COALESCE([CYCLE].[CYCLE], 18)
ORDER BY COALESCE([CYCLE].[CYCLE], 18)